"use strict";
// Creating an Array
let myFavFood = ["Pasta", "Pizza", "Burger", "Chowmein"];
// Print by using For-Loop
for (let favFood_one of myFavFood) {
    console.log(`\tI Like ${favFood_one} very much.\n`);
}
// Printing the message outside the For-Loop
console.log("\tThese are my Fav Foods.");
